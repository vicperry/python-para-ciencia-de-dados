nome = 'Alice da Silva Xavier'
#[start:end:step]
print(nome[0]) #indice
print(nome[:9]) #do começo até o índice 9
print(nome[10:]) #do indice ao fim
print(nome[10:10]) #do indice ao indice
print(nome[10:10:2]) #do indice ao indice a cada 2 passos
print(nome[:]) #do inicio ao fim
print(nome[::-1]) #o texto ao contrário, pois o passo 1 está negativo

